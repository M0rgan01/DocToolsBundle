declare(strict_types=1);

namespace Tests\Resources\Domain\Tax\Command;

use Tests\Resources\Domain\Tax\ValueObject\TaxId;

/**
 * Edits given tax with provided data (uses strong typing)
 */
class EditTaxCommand
{
    private $taxId;
    private $localizedNames;
    private $rate;
    private $enabled;

    public function __construct(int $taxId)
    {
        $this->taxId = new TaxId($taxId);
    }

    public function getTaxId(): TaxId
    {
        return $this->taxId;
    }

    public function getLocalizedNames(): ?array
    {
        return $this->localizedNames;
    }

    public function setLocalizedNames(array $localizedNames): self
    {
        $this->localizedNames = $localizedNames;

        return $this;
    }

    public function getRate(): ?float
    {
        return $this->rate;
    }

    public function setRate(float $rate): self
    {
        $this->rate = $rate;

        return $this;
    }

    public function isEnabled(): ?bool
    {
        return $this->enabled;
    }

    public function setEnabled(bool $enabled): self
    {
        $this->enabled = $enabled;

        return $this;
    }
}
